#include "choice.def"
subroutine periodic(B)


        use parameters

!**********************************************************************************************************
	implicit none    
#ifdef ONE
        real(8) :: B(1:x_end)
#endif !ONE

#ifdef TWO
        real(8) :: B(1:x_end, 1:y_end)
#endif !TWO

!**********************************************************************************************************
!print *, 'Applying Periodic Boundary conditions'

!**********************************************************************************************************
#ifdef ONE
!**********************************************************************************************************
         B(x_end)    = B(4)
         B(x_end-1)  = B(3)
         B(1)       = B(x_end-3)
         B(2)       = B(x_end-2)


#endif !ONE


!**********************************************************************************************************
#ifdef TWO
!**********************************************************************************************************
         B(x_end,:)    = B(4,:)
         B(x_end-1,:)  = B(3,:)
         B(1,:)        = B(x_end-3,:)
         B(2,:)        = B(x_end-2,:)

         B(:,y_end)    = B(:,4)
         B(:,y_end-1)  = B(:,3)
         B(:,1)        = B(:,y_end-3)
         B(:,2)        = B(:,y_end-2)

#endif !TWO






!**********************************************************************************************************
end subroutine periodic 
