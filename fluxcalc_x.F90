#include "choice.def"
subroutine fluxcalc_x(u_l, u_r)

!**********************************************************************************************************
	use Parameters
	use Variables	

!**********************************************************************************************************
	implicit none
	integer :: i, j
#ifdef ONE
        real(8) :: u_l(1:x_end),u_r(1:x_end)
#endif !ONE

#ifdef TWO
        real(8) :: u_l(1:x_end,1:y_end),u_r(1:x_end,1:y_end)
#endif !TWO



!**********************************************************************************************************
#ifdef ONE
!**********************************************************************************************************
            do i =2,x_end-1
                F_left(i)   = soundspeed * u_l(i) 
                F_right(i)  = soundspeed * u_r(i) 

            enddo 
#endif !ONE

!**********************************************************************************************************
#ifdef TWO
!**********************************************************************************************************
            do i =2,x_end-1
                do j=2,y_end-1
                        F_left_x(i,j)   = soundspeed_x * u_l(i,j)*dy(5) 
                        F_right_x(i,j)  = soundspeed_x * u_r(i,j)*dy(5)

                enddo
            enddo 
#endif !TWO


return
end subroutine fluxcalc_x

