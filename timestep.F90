!**********************************************************************************************************
!            Create_mesh.F90:  Computes the mesh of the computing grid and outputs it
!**********************************************************************************************************

#include "choice.def"
subroutine timestep

!**********************************************************************************************************
	use parameters	
        use variables

!**********************************************************************************************************
	implicit none

!**********************************************************************************************************
!print *, 'Calculating the time step'

!**************************** Computes linear r mesh *************************************

#ifdef ONE
dt = 0.5*(dx(5)/soundspeed)
#endif !ONE


#ifdef TWO
dt = 0.5* dx(5)*dy(5)/DSQRT((dy(5)*soundspeed_x)**2D0 + (dx(5)*soundspeed_y)**2D0 )
#endif !TWO


return
!**********************************************************************************************************
end subroutine timestep
